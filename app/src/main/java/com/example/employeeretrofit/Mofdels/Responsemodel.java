package com.example.employeeretrofit.Mofdels;

public class Responsemodel {


    /**
     * name : test
     * salary : 123
     * age : 23
     * id : 719
     */

    private String name;
    private String salary;
    private String age;
    private String id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSalary() {
        return salary;
    }

    public void setSalary(String salary) {
        this.salary = salary;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
